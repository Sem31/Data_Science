# list basic program

list1 = [1,2,3,4]
print(list1)

#add the no. in list
list1.append(5)
print(list1)

#update the list
list1[2] = 10
print(list1)

# sort the list 
list1.sort()
print(list1)

#remove the element from list
list1.remove(10)
print(list1)

#length of the list
print(len(list1))

#pop the element form the list
list1.pop()
print(list1)

#delete the list
del list1
print("Delete the list......")
 
